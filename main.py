import telebot

TOKEN = "TOKENINGNI_BU_YERGA_QO'Y"

bot = telebot.TeleBot(TOKEN)


@bot.message_handler(commands= ['start'])                                                                                                                                        
def start(message):
    bot.send_message(message.chat.id," Salom Xush kelibsiz ZIKIR BOTMAN.>>>")


@bot.message_handler(func=lambda message: True)
def handler(message):
    text = message.text
